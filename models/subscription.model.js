import mongoose from "mongoose";
const subscriptionSchema = new mongoose.Schema(
  {
    userId: {
      type: String,
      required: true,
    },
    name: {
      type: String,
      required: true,
    },
    price: {
      type: Number,
      required: true,
    },
  },
  {
    timestamps: true,
  }
);
const Subscription = mongoose.model("Subscription", subscriptionSchema);
export default Subscription;
